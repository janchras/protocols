    private void OnSetWG810(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OnSetWG810
        
        try(WG810V2019 wg810 = new WG810V2019("COM11"))
        {
            wg810.setRemote(true);
            wg810.setAmplitude(((Number)tfAmplitude.getValue()).doubleValue(), true);
            wg810.setFrequenz(((Number)tfFrequenz.getValue()).doubleValue(), true);
            
            switch (getRadioButtonValues()) {
                case TRIANGLE -> wg810.setKurvenform(WG810V2019.FORMTRI, 0, true);
                case RECTANGLE -> wg810.setKurvenform(WG810V2019.FORMRECT, 0, true);
                case SINE -> wg810.setKurvenform(WG810V2019.FORMSIN, 0, true);
                default -> throw new AssertionError();
}

            
            wg810.setRemote(false);
            
        } catch (Exception ex) {
            Logger.getLogger(MessGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_OnSetWG810

    private void OnReadData(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OnReadData
        double ua = 0;
        try(Fluke45V2019 mg1 = new Fluke45V2019("COM10"))
        {         
            ua = mg1.getValue();
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        measureValesList.add(new MeasureValue(getRadioButtonValues(),
                ((Number)tfFrequenz.getValue()).doubleValue(),
                ((Number)tfAmplitude.getValue()).doubleValue(),
                ua));
        
        UpdateModel();
    }//GEN-LAST:event_OnReadData

    private void OnDeletData(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OnDeletData
        LData.setModel(new MeasuredValuesListModel(new ArrayList<>()));
    }//GEN-LAST:event_OnDeletData
    
    private void UpdateModel(){
        LData.setModel(new MeasuredValuesListModel(measureValesList));
    }
    
    private Waveform getRadioButtonValues(){
        if(raDreieck.isSelected())
            return Waveform.TRIANGLE;
        
        if(raRechteck.isSelected())
            return Waveform.RECTANGLE;
        
        if(raSinus.isSelected())
            return Waveform.SINE;
        
        return null;
    }
